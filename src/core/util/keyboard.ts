export function isArrowDown(e: KeyboardEvent) {
    return e.code === 'ArrowDown';
}

export function isArrowUp(e: KeyboardEvent) {
    return e.code === 'ArrowUp';
}

export function isArrowLeft(e: KeyboardEvent) {
    return e.code === 'ArrowLeft';
}

export function isArrowRight(e: KeyboardEvent) {
    return e.code === 'ArrowRight';
}

export function isEnter(e: KeyboardEvent) {
    return e.code === 'Enter';
}

export function isEsc(e: KeyboardEvent) {
    return e.code === 'Escape';
}

export function isSpace(e: KeyboardEvent) {
    return e.code === 'Space';
}
